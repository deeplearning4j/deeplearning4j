def local_conv(input, kernel, output_size, stride, bias=None, data_format='channels_last'):
    batch_size, in_height, in_width, in_channels = input.shape
    out_height, out_width = output_size
    kernel_height = 2
    kernel_width = 2
    out_channels = 5
    kernel_shape = [kernel_height,kernel_width]
    if data_format == 'channels_first':
        input = input.transpose(0, 2, 3, 1)

    xs = []
    output_axes_ticks = [range(axis_size) for axis_size in output_size]
    positions = np.array(np.meshgrid(*output_axes_ticks)).T.reshape(-1, len(output_size))

    for position in positions:
        slices = [slice(None)]

        if data_format == 'channels_first':
            slices.append(slice(None))

        for d in range(len(output_size)):
            start = position[d] * stride[d]
            end = start + kernel_shape[d]
            slices.append(slice(start, end))

        if data_format == 'channels_last':
            slices.append(slice(None))

        input_slice = input[tuple(slices)]
        reshaped_slice = input_slice.reshape(1, -1, in_channels * kernel_height * kernel_width)
        xs.append(reshaped_slice)

    x_aggregate = np.concatenate(xs, axis=0)
    second_input = kernel.reshape(-1, out_channels)
    output = np.matmul(x_aggregate, kernel)

    new_shape = list(output_size) + [-1, out_channels]
    output = output.reshape(new_shape)

    if data_format == 'channels_first':
        permutation = list(range(len(output_size) + 1, len(new_shape))) + list(range(len(output_size)))
    else:
        permutation = [len(output_size)] + list(range(len(output_size))) + [len(output_size) + 1]

    output = output.transpose(permutation)

    if bias is not None:
        if data_format == 'channels_first':
            output += bias.reshape((1, out_channels, 1, 1))
        else:
            output += bias.reshape((1, 1, 1, out_channels))

    return output



